import User from "../models/User.js";
import bcrypt from "bcryptjs";
import jwt from "jsonwebtoken";

// üîê helper ‡∏™‡∏£‡πâ‡∏≤‡∏á JWT
function signToken(user) {
  return jwt.sign(
    { id: user._id, role: user.role }, // ‡∏ù‡∏±‡∏á role ‡∏î‡πâ‡∏ß‡∏¢
    process.env.JWT_SECRET,            // ‚úÖ ‡∏Ñ‡∏ß‡∏£‡∏ï‡∏±‡πâ‡∏á‡πÉ‡∏ô .env ‡πÄ‡∏ó‡πà‡∏≤‡∏ô‡∏±‡πâ‡∏ô
    { expiresIn: process.env.JWT_EXPIRE || "7d" }
  );
}

// ‚úÖ REGISTER
export const register = async (req, res) => {
  try {
    const { name, email, password, role } = req.body;

    // ‡∏õ‡πâ‡∏≠‡∏á‡∏Å‡∏±‡∏ô null/undefined
    if (!name || !email || !password) {
      return res
        .status(400)
        .json({ message: "‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏Å‡∏£‡∏≠‡∏Å‡∏ä‡∏∑‡πà‡∏≠ ‡∏≠‡∏µ‡πÄ‡∏°‡∏• ‡πÅ‡∏•‡∏∞‡∏£‡∏´‡∏±‡∏™‡∏ú‡πà‡∏≤‡∏ô‡πÉ‡∏´‡πâ‡∏Ñ‡∏£‡∏ö" });
    }

    // ‡∏ï‡∏±‡∏î‡∏ä‡πà‡∏≠‡∏á‡∏ß‡πà‡∏≤‡∏á
    const cleanName = String(name).trim();
    const cleanEmail = String(email).trim().toLowerCase();

    // ‡∏ï‡∏£‡∏ß‡∏à‡∏≠‡∏µ‡πÄ‡∏°‡∏•‡∏ã‡πâ‡∏≥
    const exist = await User.findOne({ email: cleanEmail });
    if (exist) {
      return res.status(400).json({ message: "‡∏≠‡∏µ‡πÄ‡∏°‡∏•‡∏ô‡∏µ‡πâ‡∏ñ‡∏π‡∏Å‡πÉ‡∏ä‡πâ‡πÅ‡∏•‡πâ‡∏ß" });
    }

    // ‡πÄ‡∏Ç‡πâ‡∏≤‡∏£‡∏´‡∏±‡∏™‡∏£‡∏´‡∏±‡∏™‡∏ú‡πà‡∏≤‡∏ô
    const hashed = await bcrypt.hash(password, 10);

    // ‡∏≠‡∏ô‡∏∏‡∏ç‡∏≤‡∏ï‡πÅ‡∏Ñ‡πà role ‡∏ó‡∏µ‡πà‡πÄ‡∏£‡∏≤‡∏Å‡∏≥‡∏´‡∏ô‡∏î
    const allowedRoles = ["jobseeker", "employer"];
    const finalRole = allowedRoles.includes(role) ? role : "jobseeker";

    const user = await User.create({
      name: cleanName,
      email: cleanEmail,
      password: hashed,
      role: finalRole,
    });

    const token = signToken(user);

    return res.status(201).json({
      message: "‡∏™‡∏°‡∏±‡∏Ñ‡∏£‡∏™‡∏°‡∏≤‡∏ä‡∏¥‡∏Å‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à",
      token,
      user: {
        id: user._id,
        name: user.name,
        email: user.email,
        role: user.role,
      },
    });
  } catch (err) {
    console.error("REGISTER ERROR:", err);
    return res
      .status(500)
      .json({ message: "‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡∏à‡∏≤‡∏Å‡πÄ‡∏ã‡∏¥‡∏£‡πå‡∏ü‡πÄ‡∏ß‡∏≠‡∏£‡πå (register)" });
  }
};

